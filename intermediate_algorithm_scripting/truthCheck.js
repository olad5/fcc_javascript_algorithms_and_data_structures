// Everything Be True
function truthCheck(collection, pre) {
  let newArr = [];
  for ( let n in collection) {
    // this adds all the Booleans the whole collection.pre
    newArr.push(Boolean(collection[n][pre]));
  }
  return newArr.every(x=> x==true);
}

